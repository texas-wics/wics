<h2>Notice to Employers</h2>
	<div class="content">
		<p>
			Thank you for your interest in the Department of Computer Science at UT Austin! Student organizations are focused on internal initiatives and academics and are not able to handle recruiting requests directly. However, the department has established effective recruiting mechanisms to assist with recruiting needs:
		</p>
		<p>
			1. College of Natural Sciences Career Services (CNS CS) at <a href="http://cns.utexas.edu/careers/career-services/employers">http://cns.utexas.edu/careers/career-services/employers</a>; contact <a href="mailto:recruit@cns.utexas.edu">recruit@cns.utexas.edu</a>. Services provided include: job postings, searchable resume database, info session scheduling, interview rooms and schedules, Career Fairs. All CNS CS job postings are publicized to CS students so there is no need to duplicate this publicity with requests to student organizations.
		</p>
		<p>
			2. Employers who want to support UTCS as a top-ten ranked department providing talent and research to industry as well as benefit from facilitated relationships with faculty, staff and students can consider the Friends of Computer Science (FoCS) program. The FoCS program is managed at the department level and provides targeted and customized ways to reach our talent by directly engaging in department life, programs, and activities. The program supports industry interests with a strategic blend of brand building, facilitated relationships, departmental events, and marketing and publicity. <a href="http://www.cs.utexas.edu/giving-collaboration/focs">http://www.cs.utexas.edu/giving-collaboration/focs</a>; contact <a href="http://www.google.com/recaptcha/mailhide/d?k=01-ZKPOW9pjl1dKs_XtqI43g==&amp;c=FoJO4Z6Pp7z1D7GnZQdEBHFO74bz5zI0TfpNt52wN_Q=" onclick="window.open('http://www.google.com/recaptcha/mailhide/d?k\07501-ZKPOW9pjl1dKs_XtqI43g\75\75\46c\75FoJO4Z6Pp7z1D7GnZQdEBHFO74bz5zI0TfpNt52wN_Q\075', '', 'toolbar=0,scrollbars=0,location=0,statusbar=0,menubar=0,resizable=0,width=500,height=300'); return false;" title="E-mail FoCS">FoCS mailing list</a>.
